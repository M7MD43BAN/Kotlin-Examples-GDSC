fun main() {
    val message = "welcome world" //String
    println(message)

    val char: Char = '$' //Character
    println(char)

    val number = 5  //Integer
    val number2: Short = -5  //Short
    val number3: Byte = 5   //Byte
    val number4 = 5L   //Long
    val x = -5L

    val pi = 3.14F  //Float
    val pi2 = 3.14  //Double

    var age: ULong = 21U

    val y = true
    val z = false

    println(y && z)
    println(y || z)
    println(!y)
    println(!z)
}